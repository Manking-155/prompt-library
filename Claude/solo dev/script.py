# Create the master ZIP file containing everything
def create_master_system():
    zip_buffer = io.BytesIO()
    
    with zipfile.ZipFile(zip_buffer, 'w', zipfile.ZIP_DEFLATED) as zip_file:
        # Add master documentation files
        for file_path, content in master_system_files.items():
            full_path = f"complete_solo_developer_system/{file_path}"
            zip_file.writestr(full_path, content)
        
        # Add existing ZIP files as nested files
        for zip_filename in zip_files_to_include:
            if os.path.exists(zip_filename):
                zip_file.write(zip_filename, f"complete_solo_developer_system/{zip_filename}")
    
    zip_buffer.seek(0)
    return zip_buffer.getvalue()

